    public void showComboBoxBarang() throws SQLException {
        DefaultComboBoxModel dcbmBarang = new DefaultComboBoxModel();
        for (Barang p : transaksi.getDataBarang()) {
            dcbmBarang.addElement(p.getNama_barang() + "(STOK " + p.getStok() + ")");
        }
        ComboBox_T.setModel(dcbmBarang);
    }

    public void showComboBoxLogin() throws SQLException {
        DefaultComboBoxModel dcbmLogin = new DefaultComboBoxModel();
        for (LoginModel p : transaksi.getDataLogin()) {
            dcbmLogin.addElement(p.getUsername());
        }
        ComboBoxO.setModel(dcbmLogin);
    }

    public void showHargaTotal() {
        Harga_total.setText(hargatotal.toString());
    }

    public double getKembalian() {
        hargatotal = Double.valueOf(Harga_total.getText());
        uang_bayar = Double.valueOf(Uang_bayar.getText());
        kembalian = 0.0;
        if (hargatotal > uang_bayar) {
            JOptionPane.showMessageDialog(this, "cek pembayaran kembali!");
        } else if (uang_bayar > hargatotal) {
            kembalian = uang_bayar - hargatotal;
            Kembalian.setText(String.valueOf(kembalian));
        }
        return kembalian;
    }

    public void showTableKeranjang() {
        DefaultTableModel dtmKeranjang = new DefaultTableModel(new String[]{"Nama Produk", "Harga Satuan",
            "Jumlah"}, 0);
        for (Detail_TransaksiModel dp : arrKeranjang) {
            dtmKeranjang.addRow(new String[]{dp.getBarang().getNama_barang(),
                dp.getBarang().getHarga_barang().toString(), dp.getJumlah().toString()
            });
        }
        TblKeranjang.setModel(dtmKeranjang);
    }

    public void showTableTransaksi() throws SQLException {
        DefaultTableModel dtmTransaksi = new DefaultTableModel(new String[]{
            "Tanggal Transaksi"}, 0);
        for (TransaksiModel p : transaksi.getDataTransaksi()) {
            dtmTransaksi.addRow(new String[]{
                p.getTgl_transaksi().toString()
            });
        }
        tblTransaksi.setModel(dtmTransaksi);
    }

