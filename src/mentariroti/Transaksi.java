            try {
                TransaksiModel pemesanan = this.transaksi.getDataTransaksi().get(this.tblTransaksi.getSelectedRow());
                String teks = "" + "========================PELANGGAN========================" + "\n"
                + "Nama Depan : " + pemesanan.getLogin().getUsername()+ "\n"
                + "========================PEMESANAN========================" + "\n"
                + "Tanggal Pemesanan : " + new SimpleDateFormat("dd/MM/yyy").format(
                    pemesanan.getTgl_transaksi()) + "\n"
                + "Harga Total : " + pemesanan.getTotal_harga()+ "\n"
                + "Produk : " + "\n";

                ArrayList<Detail_TransaksiModel> dp = pemesanan.getArrDetail_Transaksi();
                for (int i = 0; i < dp.size(); i++) {
                    teks += "\n\n" + (i + 1) + ". "
                    + "Jenis Produk : " + dp.get(i).getBarang().getJenis_barang().getNama_JenisBarang()+ "\n"
                    + "Nama Produk : " + dp.get(i).getBarang().getNama_barang()+ "\n"
                    + "Harga Produk : " + dp.get(i).getBarang().getHarga_barang()+ "\n"
                    + "Stok Produk : " + dp.get(i).getBarang().getStok()+ "\n"
                    + "Jumlah Beli Produk : " + dp.get(i).getJumlah() ;
                }
                this.jTextArea1.setText(teks);
            } catch (SQLException ex) {
                System.out.println(ex);
            }
